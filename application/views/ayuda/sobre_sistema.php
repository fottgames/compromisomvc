						<h4>Bienvenida</h4>
						<p>Estimado(a) académico (a):</p>
						<p>Junto con saludarle, le informamos a usted, que nuestra universidad dio  inicio a la evaluación del desempeño académico 2016 el cual se fundamenta en tres documentos oficiales:</p>
						<p>
							<ol>
								<li>Decreto exento 0417/2007 Evaluación y calificación del desempeño académico</li>
								<li>Decreto exento 1648/2008 Pauta de evaluación y calificación del desempeño académico</li>
								<li>Decreto Exento 0524/2009 Convenio anual de desempeño</li>
							</ol>
						</p>
						<p>Dado que Usted es la parte fundamental del proceso de evaluación del desempeño académico,  pensamos  que vuestro  rol es  clave para alinear  el quehacer diario con los objetivos estratégicos de nuestra universidad.</p>
						<p>Actualmente  nuestro proceso de evaluación, solo se ha llevado a cabo a partir de dos fuentes de información (letra a y b) tal cual lo explicita el Decreto exento 0417/2012 en su artículo 6 dejando fuera uno de los puntos  que a nuestro parecer es el fundamental para el éxito de la gestión universitaria, el relacionado con el cumplimiento del compromiso académico, el cual tiene asignado el mayor porcentaje de logro, según acuerdo del Consejo Académico (2008).</p>
						<p>
							<ul>
								<li>20% la evaluación de los estudiantes</li>
								<li>35% la autoevaluación de los académicos</li>
								<li>45% el cumplimiento del compromiso académico</li>
							</ul>
						</p>
						<p>Es por ello que se hace imperiosa la necesidad de retomar la  solicitud del  convenio anual de desempeño académico donde se estipulan los compromiso adquiridos por el  académico (a) que contrae con la institución para el cumplimiento de sus obligaciones académicas, orientadas a incrementar la productividad de la universidad, en los ámbitos de la investigación, vinculación con el medio y extensión, perfeccionamiento (Decreto 524/2009). </p>
						<p>Dada la necesidad de cumplir con este compromiso adquirido por  la Universidad, es que se hace imperiosa la necesidad de solicitar formalmente el documento relacionado con el convenio anual de desempeño con sus respectivas evidencias que avalen lo realizado.</p>
						<p>Para ello en una primera instancia se llevará a cabo un proceso de marcha blanca donde se necesitará de su apoyo y compromiso para cumplir con dicha meta. Además, le recordamos a Usted que todo compromiso adquirido debe estar avalado por las evidencias de cada área de evaluación .Para facilitar la incorporación de las evidencias al convenio anaul de desempeño se  diseñó un sistema on line que le permitirá subir las evidencias de manera on line.  Dicho material será verificado a partir de los   antecedentes entregados en su convenio anual de desempeño y  su autoevaluación (evidencias relacionadas con la docencia). </p>
						<p>Para evaluar la docencia, dentro del sistema on line aparece un apartado donde Usted deberá completar de acuerdo a lo solicitado. Se debe aclarar dentro de este tópico que si  Usted trabaja en una carrera innovada solo debe anexar el programa del módulo (ya que la planificación se encuentra inserta) y de no ser así deberá anexar además del programa de asignatura la planificación del mismo (carrera no innovada).</p>
						<p>De antemano, agradecemos su participación, colaboración y apoyo en este importante proceso. </p>
